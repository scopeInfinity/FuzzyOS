static struct {
    int size; // = width = height
} gfont = {8};

static void draw_font(char c, int x, int y) {
    int color = getcolor();

    for (int j = 0; j < 8; j++) if(y+j>=0 && y+j<GRAPHICS_MAX_HEIGHT)  {
        uint8_t mask=0b00000001;
        uint8_t frow = font[c][j];
        for(int i=0; i < 8; i++) if(x+i>=0 && x+i<GRAPHICS_MAX_WIDTH) {
            if((frow&mask)) {
                BUFFER[y+j][x+i] = color;
            }
            mask<<=1;
        }
    }
}

int textheight(char *str) {
    return gfont.size;
}

int textwidth(char *str) {
    return gfont.size * strlen(str);
}

void outtext(char *str) {
    gstate.x += outtextxy(gstate.x, gstate.y, str);
}

int outtextxy(int x, int y, char *str) {
    // returns textwidth
    int len = 0;
    while (str!=NULL && (*str)!=NULL) {
        draw_font(*str, x, y);
        x += gfont.size;
        len++;
        str++;
    }
    _autoflushnow();
    return len * gfont.size;
}

